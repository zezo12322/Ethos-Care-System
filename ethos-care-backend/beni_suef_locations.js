/**
 * يبني قائمة مسطّحة بكل المواقع المطلوب إضافتها (مراكز + قرى) دون تكرار داخلي.
 * @returns {Array<{name:string,type:string,region:string,status:string}>}
 */
function buildLocations() {
  const out = [];
  const seen = new Set();
  const push = (name, type, region) => {
    const key = `${type}|${name}|${region}`;
    if (seen.has(key)) return;
    seen.add(key);
    out.push({ name, type, region, status: 'مفعل' });
  };

  for (const [center, villages] of Object.entries(CENTERS)) {
    push(center, 'مركز', GOVERNORATE);
    for (const village of villages) {
      push(village, 'قرية', center);
    }
  }
  return out;
}

module.exports = { GOVERNORATE, CENTERS, buildLocations };
